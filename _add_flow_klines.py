# Insert flowKlines into each stock entry
# We look for the flow10d line in each stock and insert flowKlines before it
# The pattern: each stock has "flow10d":[...], then flowTodayMain...

lines = content.split('\n')
result = []
idx = 0  # which stock we're on
# Find the START of each stock after the data arrays, up to gen_report

# Strategy: Find each occurrence of "flow10d": and insert flowKlines before it
# But with 10 stocks, this is regular. Let me use a different approach:
# Insert flowKlines right after each "industry" line which is unique per stock.

# Better: find each line with "flowTodayMain":, go back to "flow10d": line,
# and insert flowKlines after "flow10d":[...] line.

# Actually simplest: find each "flowTodayMain" line, find the preceding "flow10d" line,
# and add flowKlines after the flow10d array.

# Let me just find "flow10d": and replace with flowKlines + flow10d
import re

stock_idx = -1
def add_flow_klines(match):
    global stock_idx
    stock_idx += 1
    if stock_idx < len(flow_klines_by_stock):
        klines = flow_klines_by_stock[stock_idx]
        klines_str = ",\n".join(f'    "{k}"' for k in klines)
        return f'  "flowKlines":[\n{klines_str}\n  ],\n  "flow10d":'
    return match.group(0)

content = re.sub(r'"flow10d":', add_flow_klines, content)

with open(FPATH, 'w', encoding='utf-8') as f:
    f.write(content)

count = stock_idx + 1
